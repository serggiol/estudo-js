/*
Adição (+): Soma dois valores ou concatena duas strings.
Subtração (-): Subtrai um valor de outro valor.
Multiplicação (*): Multiplica dois valores.
Divisão (/): Divide um valor por outro valor.
Resto (%) ou módulo: Retorna o resto da divisão entre dois valores.
Incremento (++) e Decremento (--): Adiciona ou subtrai 1 de um valor.
Exponenciação (**): Eleva um valor a uma potência.
*/

let a = 10;
let b = 5;

let soma = a + b; // 15
let subtracao = a - b; // 5
let multiplicacao = a * b; // 50
let divisao = a / b; // 2
let resto = a % b; // 0
let exponenciacao = a ** 2; // 121