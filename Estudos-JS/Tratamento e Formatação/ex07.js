var nome = "Sérgio"; // cria uma variável "nome" e atribui o valor "Sérgio"
var idade = 20; // cria uma variável "idade" e atribui o valor 20
var nota = 10; // cria uma variável "nota" e atribui o valor 10

// exibe no console a frase concatenando as variáveis utilizando o operador "+"
console.log("O aluno " + nome + " de " + idade + " anos tirou nota " + nota + " em seu teste");

// exibe no console a mesma frase utilizando template literals "${}"
console.log(`O aluno ${nome} de ${idade} anos tirou nota ${nota} em seu teste`);
