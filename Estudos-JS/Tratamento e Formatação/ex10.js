// Armaneza o valor "1050.5" dentro da variavel "n1"
var n1 = 1050.5

// Imprime o valor da variavel
console.log(n1) // 1050.5

//"toFixed()" serve para adicionar casas decimais,
//mude o valor dentro dos parenteses para aumentar casas
console.log(n1.toFixed(2)) // Adicionou 2 casas decimais

